document.getElementById("complete-1").addEventListener(
  "click",

  function () {
    let button = document.getElementById("complete-1");
    let numOfTask = document.getElementById("task").innerText;
    let taskDone = document.getElementById("taskDone").innerText;

    // Total Task Done
    let totalNumberOfTask = parseInt(taskDone) + 1;
    document.getElementById("taskDone").innerText = totalNumberOfTask;
    console.log(totalNumberOfTask);

    // Total Task Left
    let taskLeft = parseInt(numOfTask) - 1;
    document.getElementById("task").innerText = taskLeft;
    console.log(taskLeft);

    // Activity Log
    let now = new Date();
    let hours = now.getHours();
    let minutes = now.getMinutes();
    let seconds = now.getSeconds();

    const taskName = document.getElementById("taskName-1").innerText;

    const container = document.getElementById("log");
    const p = document.createElement("p");
    p.innerText = `You have complete the task ${taskName} at ${hours}:${minutes}:${seconds}`;
    container.appendChild(p);

    button.disabled = true;

    if (numOfTask == 1) {
      alert("congrats!!! You have Complete all of the tasks.");
    } else {
      alert("Task Completed Successfully");
    }
  }
);

